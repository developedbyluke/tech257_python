x = 5
y = 2

# ARITHMETIC

# Addition
result = x + y + 2
print(f"x + y + 2 = {result}")

# Subtraction
result = x - y - 2
print(f"x - y - 2 = {result}")

# Multiplication
result = x * y
print(f"x * y = {result}")

# Division
result = x / y
print(f"x / y = {result}")

# Modulus
result = x % y
print(f"x % y = {result}")

# COMPARISON

result = x > y
print(f"x > y = {result}")

result = x < y
print(f"x < y = {result}")

result = x >= y
print(f"x >= y = {result}")

result = x <= y
print(f"x <= y = {result}")

result = x != y
print(f"x != y = {result}")

result = x == y
print(f"x == y = {result}")
