def addition(num1: int | float, num2: int | float) -> int | float:
    return num1 + num2


def subtraction(num1: int | float, num2: int | float) -> int | float:
    return num1 - num2


def multiplication(num1: int | float, num2: int | float) -> int | float:
    return num1 * num2


def division(num1: int | float, num2: int | float) -> int | float:
    return num1 / num2
